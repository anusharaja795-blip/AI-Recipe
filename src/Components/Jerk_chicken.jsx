function Jerk_chicken()
{
    return(
        <div>
           <h3> Jerk Chicken</h3>
<h4>Ingredients:</h4>
<ol>
<li>4 chicken thighs (bone-in, skin-on)</li>

<li>2 tbsp allspice</li>

<li>2-3 scotch bonnet peppers (or habanero), chopped</li>

<li>1 tbsp fresh thyme leaves</li>

<li>4 garlic cloves, minced</li>

<li>1 inch ginger, grated</li>

<li>2 tbsp brown sugar</li>

<li>2 tbsp soy sauce</li>

<li>2 tbsp lime juice</li>

<li>1 tsp cinnamon</li>

<li>Salt and black pepper to taste</li>

<li>2 tbsp vegetable oil</li>
</ol>

<h4>Procedure:</h4>
<ol>
<li>In a blender, combine allspice, scotch bonnet peppers, thyme, garlic, ginger, brown sugar, soy sauce, lime juice, cinnamon, salt, pepper, and oil to make a marinade paste.</li>

<li>Rub the marinade all over the chicken thighs, cover, and refrigerate for at least 4 hours or overnight for best flavor.</li>

<li>Preheat grill or oven to medium-high heat.</li>

<li>Grill or bake the chicken, turning occasionally, for 30-40 minutes until cooked through and charred in spots.</li>

<li>Serve hot with rice or vegetables.</li></ol>


        </div>
    )
}
export default Jerk_chicken;